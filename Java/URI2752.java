public class URI2752{
    public static void main(String[]args){
        String variavel = "AMO FAZER EXERCICIO NO URI";
        System.out.printf("<%s>\n",variavel);
        System.out.printf("<%30s>\n",variavel);
        System.out.printf("<%.20s>\n",variavel);
        System.out.printf("<%-20s>\n",variavel);
        System.out.printf("<%-30s>\n",variavel);
        System.out.printf("<%.30s>\n",variavel);
        System.out.printf("<%30.20s>\n",variavel);
        System.out.printf("<%-30.20s>\n",variavel);
    }
}